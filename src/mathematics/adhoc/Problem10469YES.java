package mathematics.adhoc;

import java.util.*;

/**
 * To Carry or not to Carry.
 *
 * @author Izhari Ishak Aksa
 */
public class Problem10469YES {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(a ^ b);
        }
    }

}
